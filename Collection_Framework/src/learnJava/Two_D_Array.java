// Java programs on two dymensions array nested loop.

package learnJava;

public class Two_D_Array 
{
	public static void main(String[] args)
	{
		int row , colunm;
		for(row = 1 ; row<=5 ; row++) // 1
		{
			for(colunm = 1 ; colunm<=5 ; colunm++)
			{
				System.out.print("* ");
			}
			
			System.out.print("\n");
		}
		

	}

}
